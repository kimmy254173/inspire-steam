# Program to show for loops in python

import math


for x in range(-180,180,30):
    print(math.cos(x))


for x in range(-180,180,30):
    print(math.sin(x))


for x in range(-180,180,30):
    print(math.tan(x))


for x in range(-180,180,30):
    print(f"cosine of {x} ={math.cos(x)} ")








